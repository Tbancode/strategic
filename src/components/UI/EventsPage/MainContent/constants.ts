export function generateMonthArray() {
  const months = [
    { value: 'january', label: 'January' },
    { value: 'february', label: 'February' },
    { value: 'march', label: 'March' },
    { value: 'april', label: 'April' },
    { value: 'may', label: 'May' },
    { value: 'june', label: 'June' },
    { value: 'july', label: 'July' },
    { value: 'august', label: 'August' },
    { value: 'september', label: 'September' },
    { value: 'october', label: 'October' },
    { value: 'november', label: 'November' },
    { value: 'december', label: 'December' },
  ] as Option[];

  // Get the current month and convert it to "This month"
  const currentDate = new Date();
  const currentMonthIndex = currentDate.getMonth();
  months[currentMonthIndex].label = 'This month';

  // Clear previous years and use the current year as the starting point
  const currentYear = currentDate.getFullYear();
  const currentMonth = currentDate.getMonth();
  const currentMonthArray = months.slice(currentMonth);
  const currentYearArray = [
    {
      value: 'currentYear',
      label: currentYear.toString(),
      months: currentMonthArray,
    },
  ];

  return currentYearArray;
}

