export const filterByMonth = (events: any[], targetMonth: string) => {
  return events.filter(
    (event: { event_month: any }) =>
      event.event_month.toLowerCase() === targetMonth
  );
};

export const generateEventsByMonths = (
  events: EventsProps[],
  combinedOptions: Option[]
) => {
  const monthsToFilter = monthArray[0].months;

  // Filter events for each selected month
  const eventsByMonths = monthsToFilter.map((month) => {
    const filteredEvents = events.filter((event) => {
      const eventLocation = event.event_location.toLowerCase();

      return (
        combinedOptions.length < 1 ||
        combinedOptions.some(
          (option) =>
            (option.value === month.value &&
              event.event_month.toLowerCase() === month.value) ||
            (option.value === eventLocation &&
              event.event_month.toLowerCase() === month.value)
        )
      );
    });

    return {
      month: month,
      data: filterByMonth(filteredEvents, month.value), // Filter events for this month
    };
  });

  return eventsByMonths;
};

